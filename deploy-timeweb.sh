#!/bin/bash

# Скрипт деплоя на Timeweb через SSH

echo "=== Начинаем деплой ==="

# Путь к проекту на Timeweb
cd /Sravnipay

# 1. Сделать pull последних изменений
echo "1. Загружаем обновления с GitHub..."
git pull origin main

# 2. Установить зависимости (если есть новые)
echo "2. Устанавливаем зависимости..."
npm install

# 3. Собрать проект
echo "3. Собираем проект..."
npm run build

# 4. Перезапустить PM2 (если используется)
echo "4. Перезапускаем приложение..."
pm2 restart all

echo "=== Деплой завершен! ==="
